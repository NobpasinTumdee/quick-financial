/** Returns a deterministic daily level based on today's date */
export function getDailyLevel(): LevelConfig {
  const now = new Date()
  const seed = now.getFullYear() * 10000 + (now.getMonth() + 1) * 100 + now.getDate()
  return LEVELS[seed % LEVELS.length]
}
